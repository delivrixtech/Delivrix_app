function overviewStatus(overview: AdminOverview): AdminPanelStepStatus {
  if (overview.state === "critical") {
    return "blocked";
  }

  if (overview.state === "warning") {
    return "needs_review";
  }

  return "ready";
}

function fleetStatus(overview: AdminOverview): AdminPanelStepStatus {
  if (overview.health.some((decision) => decision.severity === "critical")) {
    return "blocked";
  }

  if (overview.health.some((decision) => decision.severity === "warning")) {
    return "needs_review";
  }

  return "ready";
}

function fleetStatusReason(overview: AdminOverview): string {
  const critical = overview.health.filter((decision) => decision.severity === "critical").length;
  const warning = overview.health.filter((decision) => decision.severity === "warning").length;

  if (critical > 0) {
    return `${critical} sender node health decision(s) are critical.`;
  }

  if (warning > 0) {
    return `${warning} sender node health decision(s) require review.`;
  }

  return "Sender node health is nominal.";
}

function safetyStatus(input: AdminPanelWorkflowInput): AdminPanelStepStatus {
  if (
    input.operatingNorth.delivrixSendsRealEmail
    || input.operatingNorth.liveInfrastructureWritesEnabled
    || input.operatingNorth.nfcProductionWritesEnabled
  ) {
    return "blocked";
  }

  if (input.killSwitch.enabled) {
    return "needs_review";
  }

  return "ready";
}

function safetyStatusReason(input: AdminPanelWorkflowInput): string {
  if (input.operatingNorth.delivrixSendsRealEmail) {
    return "Real Delivrix email sending is enabled and violates MVP boundary.";
  }

  if (input.operatingNorth.liveInfrastructureWritesEnabled) {
    return "Live infrastructure writes are enabled and violate MVP boundary.";
  }

  if (input.operatingNorth.nfcProductionWritesEnabled) {
    return "NFC production writes are enabled and violate MVP boundary.";
  }

  if (input.killSwitch.enabled) {
    return "Kill switch is active; operator should review incident context.";
  }

  return "Safety boundary is intact.";
}
