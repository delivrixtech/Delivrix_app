import type { AdminOverview } from "./admin-overview.ts";
import type { KillSwitchState } from "./kill-switch.ts";
import type { OperatingNorthSnapshot } from "./operating-north.ts";

export type AdminPanelWorkflowStepId =
  | "workflow"
  | "overview"
  | "openclaw"
  | "fleet"
  | "audit"
  | "reports"
  | "safety";

export type AdminPanelStepStatus = "ready" | "needs_review" | "blocked";

export interface AdminPanelWorkflowStep {
  id: AdminPanelWorkflowStepId;
  order: number;
  navLabel: string;
  title: string;
  operatorQuestion: string;
  purpose: string;
  dataSources: string[];
  evidenceToShow: string[];
  status: AdminPanelStepStatus;
  statusReason: string;
  nextStepId?: AdminPanelWorkflowStepId;
}

export interface AdminPanelWorkflow {
  generatedAt: string;
  phase: "5.4B-admin-panel-workflow";
  mode: "read_only";
  title: "Ruta operativa del panel Delivrix";
  summary: string;
  readBoundary: {
    allowedMethods: ["GET"];
    blockedMethods: ["POST", "PUT", "PATCH", "DELETE"];
    allowedEndpoints: string[];
  };
  steps: AdminPanelWorkflowStep[];
}

export interface AdminPanelWorkflowInput {
  overview: AdminOverview;
  operatingNorth: OperatingNorthSnapshot;
  killSwitch: KillSwitchState;
  now?: Date;
}

